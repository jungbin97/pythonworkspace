# ----------------------------------------------------------------------------
# [이코테] 곱하기 혹은 더하기 (그리디, python)
# 두수중 하나라도 0이나 1이라면 곱하기 보다 더하기
# ----------------------------------------------------------------------------
str = input()

result = int(str[0])

for i in range(1, len(str)):
    num = int(str[i])
    if num <= 1 or result <= 1:
        result += num
    else:
        result *= num
print(result)