n = int(input())

# dp 테이블 초기화
d = [5001 for _ in range(5001)]   # -1로 초기화
d[3] = 1
d[5] = 1

for i in range(6, n+1):
    d[i] = min(d[i-3], d[i-5]) + 1

print(d[n] if d[n] < 5001 else -1)