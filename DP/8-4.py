# ------------------------------------------------------------
#  피보나치 반복문 구현(DP : bottom-up)  
# ------------------------------------------------------------

# DP테이블 초기화
d = [0] * 100

# 첫 번째와 두 번째 수 1
d[1] = 1
d[2] = 1
n = 99

# Fibonacci Fuction 반복문으로 구현(bottom-up DP)
for i in range(3, n+1):
    d[i] = d[i-1] + d[i-2]

print(d[n])