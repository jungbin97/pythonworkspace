# ----------------------------------------------------------------------------
#  통나무 길이에 따라 반복되므로 통나무길이 홀수인지 짝수인지 구분하면 됨.
# ----------------------------------------------------------------------------
for test_case in range(1, int(input())+1):

    # 통나무 길이
    N = int(input())

    # 통나무 길이가 홀수 인지 짝수 인지 확인
    if N % 2 == 0:
        print("#{} Alice".format(test_case))
    else:
        print("#{} Bob".format(test_case))