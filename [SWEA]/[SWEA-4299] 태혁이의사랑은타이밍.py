# ----------------------------------------------------------------------------
# 출력값이 총 기다린 분이므로 계산 후 처리
# 시간 복잡도 : O(1) 
# ----------------------------------------------------------------------------
for test_case in range(1, int(input())+1):
    # D일 H시 M분
    D, H, M = map(int, input().split())

    result = (D - 11)*24*60 + (H-11)*60 + (M-11)
    
    if result<0:
        print("#{} {}".format(test_case, -1))
    else:
        print("#{} {}".format(test_case, result))