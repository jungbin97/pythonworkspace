# ----------------------------------------------------------------------------

for tc in range(1, int(input())+1):
    n = int(input())

    result = 0
    for i in range(1, n+1):
        # 짝수일 경우
        if i%2 == 0:
            result = result - i
        else:
            result = result + i

    print(f"#{tc} {result}")

# 요약
# 1~N까지 홀수는 더하고 짝수는 뱄을때 최종값을 출력